// Изучение PHP для начинающих | Урок #13 - Циклы For, While и Do While
    echo "<hr/>";
    for ($i = 100; $i >= 80; $i -= 2) {    // начинаем цикл со значения $i = 100 и заканчиваем на $i >= 80. на каждой последующей итерации цикла уменьшаем $i на 2
        if ($i % 5 == 0) continue;         // если i делится на 5 без остатка, завершаем текущую итерацию и начинаем новую
