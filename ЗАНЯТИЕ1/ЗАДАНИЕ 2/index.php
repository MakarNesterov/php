        echo "Цикл под номером № $i<br/>"; // выводим на каждой итерации с новым значением i. Затем производим следующую итерацию
    }

    echo "<br/>";

    $x = 1;                                                    // задаем значение переменной перед началом цикла while
    while ($x < 10) {                                          // цикл работает пока x меньше 10
