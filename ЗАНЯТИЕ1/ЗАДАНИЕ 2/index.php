        if ($x == 7)                                           // если x равен 7, ...  если x не равен 7 то продолжаем текущую итерацию внешнего цикла
            for ($y = 1; $y <= 3; $y += 1)                     // ...то запустим внутренний цикл с значения y = 1 увеличивая на 1 при последующей итерации, пока y не будет равен 3
                echo "Итерация внутреннего цикла № $y <br/>";  // во внутреннем цикле выводим значение y пока не будет соблюдено условия окончания внутреннего цикла
